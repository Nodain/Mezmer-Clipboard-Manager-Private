export interface EmojiCategory {
  id: string;
  label: string;
  icon: string;
  emojis: string[];
}

export function filterEmojis(query: string, categoryId: string): string[] {
  const trimmed = query.trim().toLowerCase();
  if (!trimmed) {
    return EMOJI_CATEGORIES.find((c) => c.id === categoryId)?.emojis ?? [];
  }

  const matchingCategories = EMOJI_CATEGORIES.filter(
    (c) => c.label.toLowerCase().includes(trimmed) || c.id.includes(trimmed),
  );
  if (matchingCategories.length > 0) {
    return matchingCategories.flatMap((c) => c.emojis);
  }

  const literal = query.trim();
  return EMOJI_CATEGORIES.flatMap((c) => c.emojis).filter((emoji) =>
    emoji.includes(literal),
  );
}

